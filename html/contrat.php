    <br> <h2>Confidentialité </h2><br>
    Les parties s'engagent à respecter la confidentialité des informations confidentielles de l'autre partie. Les parties ne divulgueront pas les informations confidentielles à des tiers sans le consentement écrit préalable de l'autre partie.<br>
    
    <br><h2>Loi applicable</h2><br>
    Le présent Contrat est régi par les lois en vigueur dans le pays où est domicilié le Marché.<br>
    
    <br><h2>Modification et validité</h2><br>
    Le présent Contrat ne peut être modifié que par écrit signé par les deux parties. Si une disposition du présent Contrat est jugée invalide ou inapplicable, les autres dispositions du présent Contrat restent en vigueur.<br>
    
