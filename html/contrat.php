    Ce contrat de vente en ligne (le «Contrat») est conclu entre [nom du marché] (le «Marché») et le vendeur qui s'inscrit sur le Marché (le «Vendeur»). Ce Contrat régit la vente de produits ou services proposés par le Vendeur sur le Marché.<br>
    
    <br><h2>Commission et Durée</h2><br>
    Le Vendeur accepte de payer une commission au Marché sur toutes les ventes réalisées sur le Marché. Le taux de commission est dégressif en fonction de la durée du contrat choisi par le Vendeur. Le Vendeur peut choisir un contrat de 3 mois, 6 mois, 1 an ou toute autre durée convenue entre les parties. Le taux de commission pour chaque durée de contrat est le suivant :<br>
    
