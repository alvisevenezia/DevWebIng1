    Contrat de 6 mois : [10% de comission]<br>
    Contrat de 1 an : [5% de comission]<br>

    <br><h2>Paiement</h2><br>
    Le Marché facturera au Vendeur la commission due sur chaque vente. <br>
    
    <br><h2>Résiliation</h2><br>
    Le Vendeur peut résilier ce Contrat avant la fin de la durée choisie moyennant le paiement d'un dédommagement correspondant à 10 % du montant total de la commission qui aurait été dûe si le contrat avait été exécuté jusqu'à son terme.<br>
    
